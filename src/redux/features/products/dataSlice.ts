export const dataSlice = createSlice({
  name: 'dataSlice',
  initialState,
  reducers: {
    // ðŸ‘‡ Reset the user's data by returning the initial state
    logout: () => initialState,

    // Update Data
    updateData: (state, action: PayloadAction<DataState>) => {
      state.user = action.payload.user;
    },

    updateActiveUser: (state, action: PayloadAction<string>) => {
      state.user.activeUser = action.payload;
    },
  },
});

export const { logout, updateData, updateActiveUser } = dataSlice.actions;
// ðŸ‘‡ Export the dataSlice.reducer to be included in the store.
export default dataSlice.reducer;
